import java.util.ArrayList;
//importamos a classe ArrayList para poder fazer uso do vetor e armazenar elementos.
public class fluxo_repeticao_while {
    /* metodo principal da classe */
    public static void main (String [] args){
        //fluxo de repetição usando o while
        int i = 0; //a variável i receber o valor zero (0)
        while(i < 5){ // Enquanto o i for menor que 5
            System.out.println(i);// imprima valor i
            i++ ;//incremento da variavel i;
        
        }
    
    }// fim do método 
}// fim da classe
